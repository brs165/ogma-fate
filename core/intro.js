  // ── Main entry ────────────────────────────────────────────
  function init() {
    injectCSS();

    var worldKey = detectWorld();
    var seqFn = SEQUENCES[worldKey];
    if (!seqFn) return; // unknown world, bail

    // Guide pages: title card only (zoom open → title card, no typing sequence).
    // Campaign pages: zoom open → full typing sequence → title card.
    var guide = isGuidePage();
    var seq = guide ? null : seqFn();

    var built = buildOverlay(worldKey);
    overlayEl = built.ov;
    var content = built.content;
    var screenEl = built.screenEl; // always use — zoom happens at start

    document.body.appendChild(overlayEl);

    // Skip handlers
    function skip() {
      done = true;
      dismiss();
    }
    overlayEl.addEventListener('click', skip);
    function keySkip(e) {
      if (['Space','Enter','Escape'].indexOf(e.code) !== -1 || e.key) {
        document.removeEventListener('keydown', keySkip);
        skip();
      }
    }
    document.addEventListener('keydown', keySkip);

    // Zoom the box open first
    var zoomPromise;
    if (screenEl) {
      zoomPromise = new Promise(function(resolve) {
        requestAnimationFrame(function() {
          requestAnimationFrame(function() {
            var targetW = Math.min(700, Math.round(window.innerWidth * 0.92));
            var targetH = Math.min(580, Math.round(window.innerHeight * 0.88));
            screenEl.style.width = targetW + 'px';
            screenEl.style.height = targetH + 'px';
            screenEl.style.padding = '60px 44px 80px';
            setTimeout(resolve, 1100);
          });
        });
      });
    } else {
      zoomPromise = Promise.resolve();
    }

    // Guide pages: skip straight to title card after zoom.
    // Campaign pages: run typing sequence first, then title card.
    if (guide) {
      zoomPromise.then(function() {
        if (!overlayEl) return;
        showTitleScreen(content, worldKey, screenEl);
      });
    } else {
      zoomPromise.then(function() {
        return runSequence(seq, content);
      }).then(function() {
        if (!overlayEl) return;
        showTitleScreen(content, worldKey, screenEl);
      });
    }
  }

  // ── Inline overlay builder ────────────────────────────────
  // Like buildOverlay but mounts inside a container, not the body.
