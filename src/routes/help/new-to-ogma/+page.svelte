    <h2 id="from-dnd">&#9876; I play D&amp;D</h2>

    <p>You already know what a TTRPG is. You understand skills, dice, and turn-based conflict. That foundation transfers. But Fate Condensed is a <em>fundamentally different kind of game</em>, and the habits that make you a good D&amp;D player will actively mislead you here. The shift isn't mechanical — it's philosophical.</p>

    <h3>The three things that trip up D&amp;D players most</h3>

    <p><strong>1. You describe first, then pick the mechanic.</strong> In D&amp;D you say "I roll Athletics." In Fate you say "I sprint across the rooftop and leap for the fire escape" — and the GM decides which skill fits. Fiction drives mechanics, not the other way around.</p>

    <p><strong>2. Failure is never boring.</strong> A missed attack in D&amp;D means nothing happens. In Fate, the GM offers "success at a cost" — you succeed, but something goes wrong. The story always moves forward.</p>

    <p><strong>3. Getting in trouble makes you stronger.</strong> In D&amp;D, you minimise bad outcomes. In Fate, you <em>accept</em> complications (compels) to earn fate points — the currency you spend to be awesome later. Embracing your character's flaws is the optimal strategy.</p>

