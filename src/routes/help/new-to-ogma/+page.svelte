    <h2 id="from-other"><i class="fa-solid fa-dice-d20" aria-hidden="true"></i> I play other RPGs</h2>

    <p>You know what a GM is, what dice do, what a skill check means. Here's what makes Fate Condensed different from whatever system you're coming from, in five concepts:</p>

    <p><strong>The Aspect Economy.</strong> Every important truth is an aspect — a short phrase. Character aspects, situation aspects, consequences. They can be invoked (spend a fate point for +2 or a reroll), compelled (accept a complication, earn a fate point), and they're always narratively true. If the room is <em>Pitch Black</em>, you cannot see. This is the core innovation — everything else flows from it.</p>

    <p><strong>Four Actions, Four Outcomes.</strong> Overcome (clear an obstacle), Create Advantage (add aspects to the scene), Attack (harm someone), Defend (resist). Each produces Fail / Tie / Succeed / Succeed with Style. No action economy, no bonus actions, no reactions.</p>

    <p><strong>Fiction First.</strong> Describe what you're doing. The table decides which skill and action fit. Then roll. If you're coming from PbtA, this is familiar. If you're coming from GURPS, it's a significant shift.</p>

    <p><strong>Stress Is Not Hit Points.</strong> Stress boxes are binary (checked/unchecked), each absorbs 1 shift, and they clear every scene. They're plot armor, not health. Consequences (Mild=2, Moderate=4, Severe=6) are the real injuries — they're aspects that persist and can be invoked against you.</p>

    <p><strong>The Fate Point Cycle.</strong> Start with Refresh (default 3). Spend to invoke aspects. Earn by accepting compels. The rhythm: <strong>accept trouble now → be awesome later.</strong></p>

