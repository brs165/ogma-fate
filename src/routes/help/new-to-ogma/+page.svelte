    <h2 id="new-ttrpg"><i class="fa-solid fa-seedling" aria-hidden="true"></i> I've never played a TTRPG</h2>

    <p>One person — the <strong>Game Master (GM)</strong> — describes a world. The others describe characters who live in it. When something uncertain happens ("I pick the lock"), you roll dice. The dice don't tell you what happens — they tell you <em>how well it goes</em>. No board. No winning. Just collaborative storytelling with dice.</p>

    <h3>What is Fate Condensed?</h3>
    <p>Fate cares about <strong>who your character is</strong>, not what they can do. Characters are described by short phrases called <strong>aspects</strong> — like <em>"Retired Detective Who Can't Stop Noticing Things."</em> Those phrases are always true and drive the game.</p>

    <h3>What you need to start</h3>
    <ul>
      <li>Four Fate dice (or a free dice app)</li>
      <li>Some tokens (pennies, poker chips — anything small)</li>
      <li>Index cards for notes</li>
      <li>One person willing to be the GM</li>
      <li>Ogma handles the rest — it generates characters, scenes, and adventures so the GM doesn't prep from scratch</li>
    </ul>

    <div class="callout callout-scenario">
      <div class="callout-title"><i class="fa-solid fa-gamepad" aria-hidden="true"></i> What a session looks like</div>
      <p>The GM describes a scene: "You're in the back room of a diner. The lights are dim. A woman you've never met is sitting at your usual table." You decide what your character does: "I sit down across from her and say nothing." The GM decides if that needs a roll: "She's sizing you up — roll Rapport to see if you make a good impression." You roll, add your skill number, and the result tells you how it goes. A session lasts 2–3 hours. Most of it is talking and deciding. Dice come out maybe 15–20 times.</p>
    </div>

