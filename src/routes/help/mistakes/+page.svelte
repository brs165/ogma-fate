  <h1>20 Common Fate Mistakes</h1>
  <p class="wiki-page-desc">Errors that trip up new GMs and D&D converts alike. Each one includes the fix and a Fate Condensed page reference.</p>

  <div class="wiki-quick-start">
    <strong>Quick version:</strong> Most Fate mistakes come from treating it like D&D. Fate is a conversation engine with dice, not a combat simulator with story. If something feels grindy, you're probably missing an aspect, a compel, or a concession.
  </div>

  <hr class="wiki-divider" />

  <!-- 1 -->
  <details class="step-block" open>
    <summary><span class="step-num">#1</span> Forgetting to invoke aspects</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> Players roll dice and accept the result without looking at their aspects. They forget they have five narrative levers that each grant +2 or a reroll.</p>
      <p><strong>The fix:</strong> Before every roll, ask: "Does any aspect on the table — yours, the scene's, a boost — apply here?" Make it a ritual. Aspects exist to be used.</p>
      <p class="wiki-cite">FCon p.12 — Invoking Aspects</p>
    </div>
  </details>

  <!-- 2 -->
  <details class="step-block" open>
    <summary><span class="step-num">#2</span> Never compelling Trouble</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> The GM never compels PC Troubles, so Fate points don't circulate. Players hoard FP and the economy stalls.</p>
      <p><strong>The fix:</strong> Compel at least one Trouble per session per PC. It's not punishment — it's the engine that keeps Fate points flowing. Players can always refuse by spending 1 FP.</p>
      <p class="wiki-cite">FCon p.14 — Compels</p>
    </div>
  </details>

  <!-- 3 -->
  <details class="step-block" open>
    <summary><span class="step-num">#3</span> Running combat like D&D</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> Turn-by-turn "I attack, they attack" with no Create Advantage actions. Combat becomes a hit-point grind.</p>
      <p><strong>The fix:</strong> Encourage Create Advantage every exchange. Stacking free invokes (+2 each) on a single big attack is how Fate combat works. One player sets up, another strikes.</p>
      <p class="wiki-cite">FCon p.20 — Create an Advantage</p>
      <p><strong>D&D comparison:</strong> In D&D, attacking is almost always optimal. In Fate, setting up an advantage and then attacking with stacked invokes is far more effective than raw attacks every round.</p>
    </div>
  </details>

  <!-- 4 -->
  <details class="step-block" open>
    <summary><span class="step-num">#4</span> Scenes without aspects</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> The GM describes a location but doesn't give it any scene aspects. There's nothing to invoke, compel, or interact with mechanically.</p>
      <p><strong>The fix:</strong> Every scene needs at least two aspects. "Dimly Lit Warehouse" and "Crates Stacked to the Ceiling" immediately give players tools and the GM compel hooks.</p>
      <p class="wiki-cite">FCon p.11 — Situation Aspects</p>
    </div>
  </details>

  <!-- 5 -->
  <details class="step-block" open>
    <summary><span class="step-num">#5</span> Setting difficulty too high</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> Opposition is routinely Great (+4) or higher. Players fail constantly and stop engaging with the fiction.</p>
      <p><strong>The fix:</strong> Default opposition is Fair (+2). Use Good (+3) for hard things, Great (+4) for exceptional challenges. A +4 peak PC should succeed at Fair most of the time — that's the design.</p>
      <p class="wiki-cite">FCon p.18 — Setting Difficulty</p>
      <p><strong>D&D comparison:</strong> D&D scales DCs from 5–30. Fate's ladder is much tighter: +0 to +8 covers the entire range. A +2 difference is huge in Fate.</p>
    </div>
  </details>

  <!-- 6 -->
  <details class="step-block" open>
    <summary><span class="step-num">#6</span> Not declaring stakes before conflicts</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> Combat starts without anyone stating what victory or defeat means. The fight drags because there's no endgame.</p>
      <p><strong>The fix:</strong> Before the first roll: "What do you win if you succeed? What happens if you lose?" State it clearly. This also enables concessions — a player can give up early and narrate their exit.</p>
      <p class="wiki-cite">FCon p.22 — Setting Up Scenes</p>
    </div>
  </details>

  <!-- 7 -->
  <details class="step-block" open>
    <summary><span class="step-num">#7</span> Ignoring concessions</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> NPCs (and PCs) fight to the last stress box. Every conflict ends in a taken-out result.</p>
      <p><strong>The fix:</strong> Conceding is a feature, not a failure. The conceding side narrates how they exit and gets 1 FP per consequence taken in the conflict. NPCs should concede when losing — it creates recurring villains.</p>
      <p class="wiki-cite">FCon p.24 — Conceding</p>
    </div>
  </details>

  <!-- 8 -->
  <details class="step-block" open>
    <summary><span class="step-num">#8</span> Treating consequences as flavour text</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> A PC takes "Broken Ribs" as a moderate consequence, but nobody invokes or compels it. It's just a label.</p>
      <p><strong>The fix:</strong> Consequences are aspects. They can be invoked against the character (+2 for enemies) and compelled for complications. Use them every scene until they're treated and recovered.</p>
      <p class="wiki-cite">FCon p.25 — Consequences</p>
    </div>
  </details>

  <!-- 9 -->
  <details class="step-block" open>
    <summary><span class="step-num">#9</span> Forgetting the two-step recovery process</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> A consequence heals automatically after some time. The treatment step is skipped entirely.</p>
      <p><strong>The fix:</strong> Step 1: Someone makes an overcome roll to treat the consequence (Fair for mild, Great for moderate, Fantastic for severe; +2 if self-treating). Step 2: After treatment, the timer starts — end of next scene (mild), end of session (moderate), end of arc (severe).</p>
      <p class="wiki-cite">FCon p.37 — Recovery</p>
    </div>
  </details>

  <!-- 10 -->
  <details class="step-block" open>
    <summary><span class="step-num">#10</span> Skipping Create Advantage entirely</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> Players only use Overcome and Attack actions. Create Advantage is never used, so aspect economy is dead.</p>
      <p><strong>The fix:</strong> Create Advantage is the most powerful action in Fate. Success with style gives two free invokes (+4 total). Teach players: "Set up first, then swing."</p>
      <p class="wiki-cite">FCon p.20 — Create an Advantage</p>
    </div>
  </details>

  <!-- 11 -->
  <details class="step-block" open>
    <summary><span class="step-num">#11</span> Too many zones</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> The GM maps out 8+ zones for a conflict. Movement becomes a slog, and half the zones never see action.</p>
      <p><strong>The fix:</strong> 2–4 zones per conflict. Each zone should be mechanically distinct (different aspects, different obstacles). If two zones feel the same, merge them.</p>
      <p class="wiki-cite">FCon p.22 — Zones</p>
    </div>
  </details>

  <!-- 12 -->
  <details class="step-block" open>
    <summary><span class="step-num">#12</span> Forgetting GM fate points</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> The GM never spends fate points for NPC invokes. NPCs feel weaker than they should because the GM is playing with one hand tied.</p>
      <p><strong>The fix:</strong> GM starts each scene with 1 FP per PC. Spend them to invoke scene aspects or NPC aspects against the party. Compel PC aspects to earn more. This is the GM economy — use it.</p>
      <p class="wiki-cite">FCon p.28 — GM Fate Points</p>
    </div>
  </details>

  <!-- 13 -->
  <details class="step-block" open>
    <summary><span class="step-num">#13</span> Writing passive aspects</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> Aspects like "Strong" or "Smart" — too vague to invoke meaningfully and impossible to compel.</p>
      <p><strong>The fix:</strong> Good aspects are double-edged. "Strongest Person in the Room" can be invoked for feats of strength and compelled when subtlety is needed. If an aspect can't be compelled, rewrite it.</p>
      <p class="wiki-cite">FCon p.9 — What Aspects Do</p>
    </div>
  </details>

  <!-- 14 -->
  <details class="step-block" open>
    <summary><span class="step-num">#14</span> Rolling when there's no cost for failure</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> Calling for a roll when failure doesn't change anything. "Roll to open the unlocked door." If there's no consequence for failure, there's no roll.</p>
      <p><strong>The fix:</strong> Only roll when both success and failure are interesting. If failure just means "try again," either succeed automatically or make failure mean something new happens.</p>
      <p class="wiki-cite">FCon p.18 — When to Roll</p>
    </div>
  </details>

  <!-- 15 -->
  <details class="step-block" open>
    <summary><span class="step-num">#15</span> Blocking player narration</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> The GM narrates everything. Players roll dice but never describe what their character does or how the aspect applies.</p>
      <p><strong>The fix:</strong> When a player invokes an aspect, they narrate how it helps. When they Create Advantage, they describe the new aspect. Fate is collaborative fiction — share the camera.</p>
      <p class="wiki-cite">FCon p.12 — Invoking Aspects</p>
      <p><strong>D&D comparison:</strong> In D&D, the DM typically narrates the result of actions. In Fate, the player who invokes or creates an aspect owns the narration of how it manifests.</p>
    </div>
  </details>

  <!-- 16 -->
  <details class="step-block" open>
    <summary><span class="step-num">#16</span> Treating stress as hit points</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> Players track stress like HP, assuming more stress boxes = more health. They forget stress clears at end of scene.</p>
      <p><strong>The fix:</strong> Stress is plot armour, not health. It represents close calls, not injuries. It clears completely at end of scene. Consequences are the real injuries. Don't confuse the two.</p>
      <p class="wiki-cite">FCon p.24 — Stress</p>
      <p><strong>D&D comparison:</strong> D&D hit points persist until healed. Fate stress resets every scene. If you take 3 stress in scene 1, you start scene 2 fresh. Only consequences carry over.</p>
    </div>
  </details>

  <!-- 17 -->
  <details class="step-block" open>
    <summary><span class="step-num">#17</span> Making Overcome the only action</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> Every challenge is "roll to overcome." No Create Advantage, no Defend, no Attack. The four actions collapse into one.</p>
      <p><strong>The fix:</strong> Use all four actions deliberately. A locked door is Overcome. Noticing a guard pattern is Create Advantage. Being shot at is Defend. Punching someone is Attack. Each action has different success/failure consequences.</p>
      <p class="wiki-cite">FCon p.18 — Actions and Outcomes</p>
    </div>
  </details>

  <!-- 18 -->
  <details class="step-block" open>
    <summary><span class="step-num">#18</span> Forgetting boosts</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> Succeed with style happens, and nobody creates a boost. Free +2s are left on the table every session.</p>
      <p><strong>The fix:</strong> Succeed with style on Attack or Overcome = you get a boost. Name it immediately ("Off Balance", "Momentary Opening"). Use it next exchange or lose it. It's free advantage — take it.</p>
      <p class="wiki-cite">FCon p.20 — Succeed with Style</p>
    </div>
  </details>

  <!-- 19 -->
  <details class="step-block" open>
    <summary><span class="step-num">#19</span> One skill solves everything</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> A player uses Fight for every problem. Intimidate someone? Fight. Break down a door? Fight. The skill pyramid becomes meaningless.</p>
      <p><strong>The fix:</strong> Skills are fictional permissions, not just numbers. Fight lets you harm someone in melee — it doesn't let you intimidate (that's Provoke) or break objects (that's Physique). Enforce the fiction that each skill represents.</p>
      <p class="wiki-cite">FCon p.15 — Skill List</p>
    </div>
  </details>

  <!-- 20 -->
  <details class="step-block" open>
    <summary><span class="step-num">#20</span> Playing scenes that don't matter</summary>
    <div class="step-body">
      <p><strong>The mistake:</strong> The party travels, shops, and chats with NPCs for 30 minutes with no aspects, no stakes, and no conflict. Nothing mechanical happens.</p>
      <p><strong>The fix:</strong> Before every scene, ask: "What's at stake? Who opposes it? What aspects are in play?" If you can't answer at least two of these, skip the scene and narrate the outcome. Fate is about meaningful moments, not filler.</p>
      <p class="wiki-cite">FCon p.22 — Setting Up Scenes</p>
    </div>
  </details>

  <hr class="wiki-divider" />

  <div class="wiki-quick-start">
    <strong>Mid-session rescue:</strong> If a session feels flat, check three things: (1) Are there scene aspects on the table? (2) Has anyone been compelled recently? (3) Are players using Create Advantage? Fix whichever one is missing and the session will recover within one scene.
  </div>

